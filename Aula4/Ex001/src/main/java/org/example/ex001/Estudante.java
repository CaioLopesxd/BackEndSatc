package org.example.ex001;

public class Estudante {
    public String nome;
    public int codigo;
    public String curso;
    public String email;
    public String telefone;

    Estudante(String nome, int codigo, String curso, String email, String telefone){
        this.nome = nome;
        this.codigo = codigo;
        this.curso = curso;
        this.email = email;
        this.telefone = telefone;
    }
}
